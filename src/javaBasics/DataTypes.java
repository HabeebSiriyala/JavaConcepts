package javaBasics;

public class DataTypes {

	
		//Main Method---->Starting And Execution Of the Program
		public static void main(String[] args) {
			
			//======> Primitive Data Types--- int, double, char, boolean
			//1.int:
			int i=10;
			int j=20;
			System.out.println(j);
			int k=30;
			int l=40;
			System.out.println(j);
			int m=50;
			
			//2.Double
			double d= 12.33;
			double d1=34.44;
			System.out.println(d1);
			double d2=100;//100.00
			System.out.println(d2);
			
			//3.char----->only single digit value,Should be written in Single Quotes
			char c='a';
			char c1='b';
			System.out.println(c1);
			char c3='c';
				
			//boolean
			boolean b1= true;
			boolean b2= false;
			System.out.println(b1);
			
			//String---- not a datatype its a class but it can be used as datatype
			String s="Hello World";
			String s1="Selenium";
			System.out.println(s1);
			String s2="Eclipse";
		
	}

}
